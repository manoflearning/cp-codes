#include <iostream>
using namespace std;

int main() {
	int n;
	scanf("%d", &n);

	for (int test = 0; test < n; test++) {
		int a, b;
		scanf("%d %d", &a, &b);

		printf("Case %d: %d\n", test + 1, a + b);
	}

	return 0;
}
